/*
  Variables (var): Las variables declaradas como variables, estas pueden cambiar el contenido asignado incialmente. No cambian durante la ejecución del programa.
 
 Constantes (let): Las variables que son declaradas como constantes, estas no pueden cambiar el contenido asignado inicialmente. Puede cambiar durante la ejecución del programa
 */

/*  Variables   */
// asignación inicial
var my_Name = "Ulises García"
// nueva asignación
my_Name = "José González"

/*  Constantes  */
// asignación inicial
let my_Edad = 25
// nueva asignación --> Obtiene error
//my_Edad = 30

/*      Declaraciones multiples     */

